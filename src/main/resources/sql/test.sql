SELECT LIEUX.ID_LIEU, LIEUX.ID_REV, LIEUX.NOM, LIEUX.DESCRIPTION
FROM LIEUX, (
    SELECT ID_LIEU, MAX(ID_REV) AS MAX_REV
    FROM LIEUX
    GROUP BY ID_LIEU
    ) a
WHERE LIEUX.ID_LIEU = a.ID_LIEU
AND LIEUX.ID_REV = a.MAX_REV
AND (LIEUX.NOM IS NOT NULL OR LIEUX.DESCRIPTION IS NOT NULL)
AND LIEUX.ID_LIEU = 1